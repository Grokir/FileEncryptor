void AES::clear_msg(std::vector<std::vector<uint>> &v){
  if(v.empty()){
    v = std::vector<std::vector<uint>>(
      this->Nb, std::vector<uint>(this->Nb, 0)
    );
    return;
  }

  for(int j = 0; j < 4; j++){
    for(int i = 0; i < 4; i++){
      v[i][j] = 0;
    }
  }
};


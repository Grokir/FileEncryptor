std::vector<std::vector<uint>> KeyExpansion(const std::vector<uint>& key, uint Nb, uint Nr, uint Nk){
  // std::vector<std::vector<uint>>  words((Nr + 1));
  std::vector<std::vector<uint>>  words((Nr + 1)*Nb, std::vector<uint>(Nb));
  std::vector<uint>               temp;

  for(int i = 0; i < Nk; i++)
    words[i] = {key[4*i], key[4*i+1], key[4*i+2], key[4*i+3]};
  
  for(int i = Nk; i < Nb * (Nr+1); i++){
    temp = words[i-1];
    if( !( i % Nk ) ){
      RotWord(temp);
      SubWord(temp);
      for(int j = 0; j < temp.size(); j++)
      // temp[j] ^= RCON_table[i/Nk][j];
      temp[j] ^= RCON_table[i/Nk + j];
    }
    else if (Nk > 6 && (i % Nk == 4)){
      SubWord(temp);
    }
    for(int j = 0; j < temp.size(); j++) 
      words[i][j] = words[i-Nk][j] ^ temp[j];
  }

  return words;
};

void AddRoundKey(std::vector<std::vector<uint>>& state, const std::vector<std::vector<uint>>& keys, uint Nb, uint round) {
// void AddRoundKey(std::vector<std::vector<uint>>& state, const std::vector<uint>& key, uint Nb) {
  for (uint8_t c = 0; c < Nb; c++) {
    for(uint8_t i = 0; i < Nb; i++){
      state[i][c] ^= keys[round * Nb + c][i];
      // state[i][c] = state[i][c]^key[c];
    }
  }
}



bool AES::encrypt(){
  try {
    
    AddRoundKey(this->msg, this->w, this->Nb, 0);

    for(uint round = 1; round < this->Nr; round++){
      SubBytes(this->msg);
      ShiftRows(this->msg);
      MixColumns(this->msg, this->Nb);
      AddRoundKey(this->msg, this->w, this->Nb, round);
    }
    SubBytes(this->msg);
    ShiftRows(this->msg);
    AddRoundKey(this->msg, this->w, this->Nb, this->Nr);

  } catch( ... ) {
    return false;
  }

  return true;
};


bool AES::decrypt(){
  const bool is_inverse = true;

  try {
    
    AddRoundKey(this->msg, this->w, this->Nb, this->Nr);
    
    for(uint round = (this->Nr - 1); round >= 1 ; round--){
      ShiftRows(this->msg, is_inverse);
      SubBytes(this->msg, is_inverse);
      AddRoundKey(this->msg, this->w, this->Nb, round);
      MixColumns(this->msg, this->Nb, is_inverse);
    }
    ShiftRows(this->msg, is_inverse);
    SubBytes(this->msg, is_inverse);
    AddRoundKey(this->msg, this->w, this->Nb, 0);
    
  } catch( ... ) {
    return false;
  }

  return true;
};



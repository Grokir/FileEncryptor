AES::AES() = default;

AES::AES(const uint& bin_key_len){
  this->size_of_key   = bin_key_len;
  
  this->msg = std::vector<std::vector<uint>>(4, std::vector<uint>(this->Nb, 0));

  if(this->size_of_key == 192)
    this->Nr = 12;
  
  if(this->size_of_key == 256)
    this->Nr = 14;
};

void AES::setKEY(const std::vector<uint>& key) {
  this->w = KeyExpansion(
    key,
    this->Nb,
    this->Nr,
    this->Nk
  );
};

void AES::setMSG(const std::string& msg) {
  this->clear_msg(this->msg);
  int msglen = msg.size();
  int k = 0;
  for(int i = 0; i < 4; i++){
      for(int j = 0; j < Nb; j++){
        this->msg[i][j] = std::bitset<8>(msg[k]).to_ulong();
        ++k;
        if (k >= msglen)
          return;
    }
  }
};

std::string AES::getMSG() {
  std::string    res = "";

  for(int j = 0; j < 4; j++)
    for(int i = 0; i < 4; i++)
      res += static_cast<char>(this->msg[i][j]);
  
  return res;
};


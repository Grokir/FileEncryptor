/// Methods

void RotWord(std::vector<uint>& w){
  int tmp = w[0];
    
  for(int j = 1; j < w.size(); j++)
    w[j-1] = w[j];
  
  w[w.size() - 1] = tmp;
};

void InvRotWord(std::vector<uint>& w){
  std::size_t sz = w.size();
  int tmp = w[sz-1];
    
  for(int j = w.size() - 1; j > 0; j--)
    w[j] = w[j-1];
  
  w[0] = tmp;
};


void ShiftRows(std::vector<std::vector<uint>>& matr, bool is_inverse=false) {
  for(int i = 1; i < matr.size(); i++)
    for(int k = 0; k < i; k++)
      (is_inverse? InvRotWord(matr[i]) : RotWord(matr[i]));
};


uint SubByte(uint byte, bool is_inverse=false) {
  std::string binstr = std::bitset<8>(byte).to_string();
  uint x = std::bitset<8>(binstr.substr(0, 4)).to_ulong();
  uint y = std::bitset<8>(binstr.substr(4, 4)).to_ulong();
  
  if(is_inverse)
    return InvSubBytes_table[x][y];
  
  return SubBytes_table[x][y];
};

void SubBytes( std::vector<std::vector<uint>>& state, bool is_inverse=false) {
  for(int i = 0; i < state.size(); i++)
    for(int j = 0; j < state[0].size(); j++)
      state[i][j] = SubByte(state[i][j], is_inverse);
};

void SubWord(std::vector<uint>& w){    
  for(int i = 0; i < w.size(); i++)
    w[i] = SubByte(w[i]);
};


// GF(2^8) multiplication
uint gf256_mult(uint lhs, uint rhs){
  uint res = lhs & (-(rhs & 1));
  for(int i = 1; i < 8; i++){
    lhs = (lhs << 1) ^ ((-(lhs >> 7)) & 0x11b);
    res = res ^ (lhs & (-((rhs >> i) & 1)));
  }
  return res;
};

void MixColumns(std::vector<std::vector<uint>>& state, uint Nb, bool is_inverse=false){
  std::vector<int> res = std::vector<int>(Nb, 0);

  for(int i = 0; i < Nb; i++){
    for(int j = 0; j < Nb; j++)
      res[i] ^= gf256_mult(
        (is_inverse? InvMixColumns_table[i][j] : MixColumns_table[i][j]),
        state[j][i]
      );
    
    for(int k = 0; k < Nb; k++)
      state[k][i] = res[k];
  }
};

 
